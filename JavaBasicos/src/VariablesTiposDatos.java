
public class VariablesTiposDatos {
	public static void main(String[] args) {
		//String permite almacenar cadena de texto
		String nombre = "Oscar Sosa";
		int edad = 31;
		//char nos permite almacenar un caracter, pero solo 1
		char inicial = 'C';
		//boolean almacena un valor booleano, puede ser true or false
		boolean tienesCrush = true;
		//tipos de datos enteros
		//byte 
		byte edadb = 31;
		byte suma = 24 + 98;
		//short
		short suma1 = 24 + 104;
		//int
		int numero = 1021321312;
		//long
		long numeroLargo = 100_000_000;
		System.out.println(numeroLargo);
		//float
		float promedio = 1232.23232f;
		//double
		double numeroPi = 3.1415934324234;
	}
}
